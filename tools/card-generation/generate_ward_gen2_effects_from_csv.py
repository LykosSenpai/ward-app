def clean_num(card_number):
    return (card_number or "")[:3]

def slug(s):
    return re.sub(r'[^a-z0-9]+','_',s.lower()).strip('_')

def bool_s(v):
    return "TRUE" if bool(v) else "FALSE"

def parse_int(x):
    if x is None or str(x).strip()=="":
        return None
    try:
        return int(str(x).strip().replace("+",""))
    except ValueError:
        return None

def normalize_magic_type(c):
    if c["Card Type"].strip().lower() != "magic":
        return ""
    raw=(c.get("Magic Type") or "").strip().lower()
    return {"single use":"STANDARD","infinite":"INFINITE","lightning":"LIGHTNING"}.get(raw, raw.upper() if raw else "STANDARD")

def magic_subtype(c):
    if c["Card Type"].strip().lower() != "magic":
        return ""
    n=clean_num(c["Card Number"])
    if n in FIELD_CARD_NUMBERS or "field card" in (c.get("Notes") or "").lower():
        return "FIELD"
    if (c.get("Is Equip") or "").strip().upper()=="TRUE":
        return "EQUIP"
    return "NONE"

def camel_to_enum(s):
    return re.sub('([a-z0-9])([A-Z])', r'\1_\2', s).upper()

def parse_condition(text):
    t=text or ""; low=t.lower()
    m=re.search(r'roll\s+1\s+die;\s*success\s+(\d)\s*-\s*(\d)', low)
    if m:
        a,b=int(m.group(1)),int(m.group(2))
        return {"type":"ROLL_DIE_RANGE","dieSize":6,"successValues":list(range(a,b+1)),"text":t}
    m=re.search(r'roll\s+1\s+die;\s*success\s+(\d)', low)
    if m:
        return {"type":"ROLL_DIE_VALUES","dieSize":6,"successValues":[int(m.group(1))],"text":t}
    if "roll 2 dice" in low:
        return {"type":"ROLL_DICE","dice":2,"dieSize":6,"text":t}
    if "opponent plays a lightning" in low or "opponent uses a lightning" in low:
        return {"type":"OPPONENT_PLAYS_LIGHTNING","text":t}
    if "opponent plays a magic" in low or "opponent plays magic" in low:
        return {"type":"OPPONENT_PLAYS_MAGIC","text":t}
    if "opponent lands a hit" in low:
        return {"type":"OPPONENT_LANDS_HIT","text":t}
    if "hit lands" in low or low == "this creature hits":
        return {"type":"HIT_LANDS","text":t}
    if "hits first" in low or "attack first" in low:
        return {"type":"HITS_FIRST","text":t}
    if "equipped to creature" in low:
        return {"type":"EQUIPPED_TO_CREATURE","text":t}
    if "summoned" in low:
        return {"type":"ON_SUMMON_CONDITION","text":t}
    if low.startswith("always") or low == "effect active":
        return {"type":"ALWAYS","text":t}
    if "field effect is active" in low or "active" in low:
        return {"type":"FIELD_EFFECT_ACTIVE","text":t}
    if "leaves field" in low or "changes control" in low or "effect is negated" in low:
        return {"type":"SOURCE_INVALIDATED","text":t}
    return {"type":"TEXT","text":t}

def parse_duration(text, group=""):
    t=text or ""; low=t.lower()
    if low == "instant": return {"text":t,"type":"INSTANT"}
    if "current battle" in low: return {"text":t,"type":"CURRENT_BATTLE"}
    if "current attack" in low: return {"text":t,"type":"CURRENT_ATTACK"}
    if "current turn" in low: return {"text":t,"type":"CURRENT_TURN"}
    if "next turn" in low: return {"text":t,"type":"NEXT_TURN"}
    if "until used" in low: return {"text":t,"type":"UNTIL_CONSUMED"}
    if "while equipped" in low: return {"text":t,"type":"WHILE_EQUIPPED"}
    if "while on field" in low or "while field" in low or "while source" in low: return {"text":t,"type":"WHILE_ON_FIELD"}
    if "before summon" in low: return {"text":t,"type":"BEFORE_SUMMON"}
    if "before equip" in low: return {"text":t,"type":"BEFORE_EQUIP"}
    if "after 1 turn cycle" in low:
        return {"text":t,"type":"TURN_CYCLES","amount":1,"unit":"TURN_CYCLE","starts":"EFFECT_ACTIVATION","expires":"BEGINNING_OF_START_PLAYER_TURN"}
    m=re.search(r'(\d+)\s+turn\s+cycles?', low)
    if m:
        d={"text":t,"type":"TURN_CYCLES","amount":int(m.group(1)),"unit":"TURN_CYCLE","starts":"EFFECT_ACTIVATION","expires":"BEGINNING_OF_START_PLAYER_TURN"}
        if "damage over time" in group.lower():
            d["tickTiming"]=GLOBAL_RULES["dotTickTiming"]; d["stackRule"]=GLOBAL_RULES["dotStacking"]
        return d
    if "turn cycle" in low:
        return {"text":t,"type":"TURN_CYCLES","unit":"TURN_CYCLE","starts":"EFFECT_ACTIVATION","expires":"BEGINNING_OF_START_PLAYER_TURN"}
    if "end of battle" in low: return {"text":t,"type":"UNTIL_END_OF_BATTLE"}
    if "end of your turn" in low: return {"text":t,"type":"UNTIL_END_OF_YOUR_TURN"}
    if "while resolving" in low: return {"text":t,"type":"WHILE_RESOLVING"}
    return {"text":t,"type":"TEXT"}

def source_linked(spec):
    if spec.get("_sourceLinked") is not None:
        return spec["_sourceLinked"]
    txt=" ".join([spec.get("Condition",""),spec.get("Value",""),spec.get("Duration",""),spec.get("Original Effect Text","")]).lower()
    return any(k in txt for k in ["leaves the field","leaves field","changes control","switches control","effect is negated","linked","under this card"])

def uses_anchoring(spec):
    if spec.get("_usesAnchoring"): return True
    txt=" ".join([spec.get("Target",""),spec.get("Value",""),spec.get("Original Effect Text","")]).lower()
    return any(k in txt for k in ["equipped","under this card","linked","stolen equip"])

def requires_response(spec):
    if spec.get("_requiresResponse"): return True
    return any(k in spec.get("Trigger","") for k in ["FROM_HAND","OPPONENT_PLAYS","OPPONENT_LANDS","WOULD_HEAL","TARGETED_BY_MAGIC"])

def action_type_for(fn):
    return SPECIAL_ACTION.get(fn, camel_to_enum(fn))

def effect_from_spec(spec, effect_index):
    condition=parse_condition(spec["Condition"])
    duration=parse_duration(spec["Duration"], spec["Effect Group"])
    stack_rule=spec.get("_stackRule") or duration.get("stackRule")
    tick_timing=spec.get("_tickTiming") or duration.get("tickTiming")
    damage_type=spec.get("_damageType") or ""
    src=source_linked(spec)
    anchor=uses_anchoring(spec)
    params={"target":spec["Target"],"valueText":spec["Value"],"statChanges":spec.get("_statChanges") or [],"condition":condition,"duration":duration,"damageType":damage_type,"sourceLinked":src,"usesAnchoring":anchor,"roundingMode":"CEIL" if any(x in (spec.get("Implementation Notes","").lower()+spec.get("Value","").lower()) for x in ["half","1/2","50%"]) else None,"stackRule":stack_rule,"tickTiming":tick_timing}
    params.update(spec.get("_paramsExtra") or {})
    notes=spec.get("Implementation Notes","")
    if "turn cycle" in spec.get("Duration","").lower() and "Turn cycle is one full round" not in notes:
        notes=(notes+" " if notes else "")+"Turn cycle is one full round back to the same player."
    return {"id":f"{spec['Card Number']}-E{effect_index:02d}","trigger":spec["Trigger"],"condition":condition,"actionType":action_type_for(spec["Reusable Function"]),"effectGroup":spec["Effect Group"],"actionText":spec["Effect Action"],"target":spec["Target"],"value":spec["Value"],"duration":duration,"reusableFunction":spec["Reusable Function"],"params":params,"notes":notes,"needsReview":bool(spec.get("_needsReview",False))}

